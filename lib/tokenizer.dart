  // --- Tokenization Method ---
  static List<int> tokenize(
  String text, {
  required VocabularyType
  vocabularyType, // Specify which vocabulary to use
        int? maxLength,
        String fieldName = 'text', // For clearer logging
      }) {
    if (kDebugMode) {
      print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Starting tokenization for text: "$text"');
    }

    final Map<String, int> activeVocabulary = _getVocabulary(vocabularyType);

    if (activeVocabulary.isEmpty) {
      if (kDebugMode) {
        print('MyTokenizer ($fieldName - Vocab: $vocabularyType): ERROR - Active vocabulary is empty. Cannot tokenize.');
      }
      return [];
    }

    if (text.trim().isEmpty) {
      if (kDebugMode) {
        print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Input text is empty. Returning empty list of tokens.');
      }
      if (maxLength != null && maxLength > 0) {
        return List<int>.filled(maxLength, padTokenId);
      }
      return [];
    }

    String processedText = text.toLowerCase();
    if (kDebugMode) {
      print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Text after lowercase: "$processedText"');
    }

    List<String> words = processedText.split(RegExp(r'\s+')).where((s) => s.isNotEmpty).toList();
    if (kDebugMode) {
      print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Split into words: $words');
    }

    List<int> tokenIds = [];
    for (String word in words) {
      String cleanedWord = word.replaceAll(RegExp(r'[^\w]'), '');
      if (cleanedWord.isNotEmpty) {
        if (activeVocabulary.containsKey(cleanedWord)) {
          tokenIds.add(activeVocabulary[cleanedWord]!);
        } else {
          tokenIds.add(unkTokenId); // Assuming unkTokenId is universal or defined per vocab
          if (kDebugMode) {
            print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Word "$cleanedWord" (original: "$word") not in active vocabulary. Using <unk> token.');
          }
        }
      }
    }
    if (kDebugMode) {
      print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Converted to token IDs (before padding/truncation): $tokenIds');
    }

    if (maxLength != null && maxLength > 0) {
      if (tokenIds.length > maxLength) {
        tokenIds = tokenIds.sublist(0, maxLength);
        if (kDebugMode) {
          print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Truncated to $maxLength tokens: $tokenIds');
        }
      } else if (tokenIds.length < maxLength) {
        int tokensToPad = maxLength - tokenIds.length;
        for (int i = 0; i < tokensToPad; i++) {
          tokenIds.add(padTokenId); // Assuming padTokenId is universal
        }
        if (kDebugMode) {
          print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Padded to $maxLength tokens: $tokenIds');
        }
      }
    } else {
      if (kDebugMode) {
        print('MyTokenizer ($fieldName - Vocab: $vocabularyType): No maxLength provided. Final tokens: $tokenIds');
      }
    }

    if (kDebugMode) {
      print('MyTokenizer ($fieldName - Vocab: $vocabularyType): Final token IDs: $tokenIds (Count: ${tokenIds.length})');
    }
    return tokenIds;
  }
